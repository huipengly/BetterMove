void CGivePosition::shoulderSetAngle(double tarAngle[], int num)
{
	for (int i = 0; i < num; i++)
	{
		double tmpTarPos = tarAngle[i] / 90.0 * 23;//测试，电机位置转动90度时，移动位置为23
		if( PositionArraySize == 0 )
		{
			if (tmpTarPos - 0 < 1e-5)//如果下一个目标点和前一个目标点一样，就不添加。
			{
				continue;
			}
			if (tmpTarPos - 0 > shoulderMaxPos)//判断添加的位置是否距离太远，超过单次移动最大距离，超过则拆分位置
			{
				int pieces = round_((tmpTarPos - 0) / shoulderMaxPos);
				for (int j = 0; j < pieces; j++)
				{
					if (PositionArraySize == 0)
					{
						MyCppPosition[PositionArraySize] = tmpTarPos / pieces;
					}
					else
					{
						MyCppPosition[PositionArraySize] = MyCppPosition[PositionArraySize - 1] + tmpTarPos / pieces;
					}
					if (PositionArraySize < 99)
					{
						PositionArraySize++;
					}
				}
			}
			else
			{
				MyCppPosition[PositionArraySize] = tmpTarPos;
				if (PositionArraySize < 99)
				{
					PositionArraySize++;
				}
			}
		}
		else
		{
			//FIXME:前后差值为负值的情况没考虑
			if (abs(tmpTarPos - MyCppPosition[PositionArraySize - 1]) < 1e-5)//如果下一个目标点和前一个目标点一样，就不添加。
			{
				continue;
			}
			if (tmpTarPos - MyCppPosition[PositionArraySize - 1] > shoulderMaxPos)//判断添加的位置是否距离太远，超过单次移动最大距离，超过则拆分位置
			{
				int pieces = static_cast<int> ((tmpTarPos - MyCppPosition[PositionArraySize]) / shoulderMaxPos);
				for (int j = 0; j < pieces; j++)
				{
					MyCppPosition[PositionArraySize] = MyCppPosition[PositionArraySize - 1] + tmpTarPos * 1 / pieces;
					if (PositionArraySize < 99)
					{
						PositionArraySize++;
					}
				}
			}
			else
			{
				MyCppPosition[PositionArraySize] = tmpTarPos;
				if (PositionArraySize < 99)
				{
					PositionArraySize++;
				}
			}
		}
	}
	//TODO:判断两个点之间距离超过限定值，就拆开
	//TODO:如果给定超过数组大小，怎么解决
}

void CGivePosition::shoulderRun()
{
	if (PositionCount < PositionArraySize)	//没有走完已有坐标，则继续移动
	{

		if (m_Inputs.CanGivenPosition)		//可以给定坐标
		{
			m_Outputs.GivenPosition = MyCppPosition[PositionCount];	//将坐标传给plc
			m_Outputs.IsGivenPositon = TRUE;						//c++给定了坐标
		}
	}


	if ((m_Outputs.IsGivenPositon) && (MyCppPosition[PositionCount] == m_Inputs.NextPosition))
		//当NextPosition已经记录了给定坐标时，认为可以继续给定，将IsGivenPositon设为假
	{
		m_Outputs.IsGivenPositon = FALSE;	//上一次给定坐标已被PLC记录，认为c++未给定下一个坐标，可以继续给定
		++PositionCount;					//将c++坐标移动到下一个
	}
}

